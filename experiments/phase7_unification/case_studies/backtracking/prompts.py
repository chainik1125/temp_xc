"""Hand-written 30-prompt seed set: 10 each of logic / geometry / probability.

Ward et al. used 300 prompts in 10 categories generated by Claude Sonnet 3.7;
we use a small deterministic seed set for the MVP and let `build_reasoning_traces`
sample with replacement if a larger N is requested. Swap this file out (or
extend `PROMPTS`) to scale to the paper's 300.

Each prompt is a dict {id, category, text}. The text is fed to the model via
the tokenizer's chat template as a single user turn.
"""

