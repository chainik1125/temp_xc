"""Generate the 300 seed prompts for Ward 2025 backtracking replication.

Ward Appendix A defers the recipe to Venhoff 2025 (arXiv 2506.18167):
  - 300 prompts, 10 categories of basic logic / geometry / probability /
    arithmetic / counting / number theory / set theory / sequences /
    inequalities / algebra word problems
  - generated by Claude Sonnet (paper used 3.7; we use 4.6 — the
    currently-available Sonnet sibling)

Why a fresh generation pass instead of pulling theirs: their prompts are
not released; matching the *recipe* is what matters for the steering
direction reproducibility, since the DoM vector averages over many traces
and is robust to prompt-set details (per Ward §3.3 baselines).

Output schema (json):
  [{"id": "logic_001", "category": "basic_logic", "prompt": "..."}, ...]
"""

from __future__ import annotations

import argparse
import asyncio
import json
import logging
import sys
from pathlib import Path

import yaml

from src.bench.venhoff.judge_client import AnthropicJudge

logging.basicConfig(level=logging.INFO, format="%(message)s")
log = logging.getLogger("ward.seed_prompts")


SYSTEM_PROMPT = (
    "You generate concise reasoning problems for a research dataset. "
    "Each problem should require multi-step reasoning, take a strong "
    "model 30-90 seconds of internal thought, and have a single "
    "well-defined answer. Avoid trivia, factual recall, or anything "
    "requiring external information. Return EXACTLY one problem per "
    "response, no preamble, no commentary, no answer key — just the "
    "problem statement as plain text."
)

CATEGORY_GUIDANCE = {
    "basic_logic": (
        "Boolean / propositional / first-order logic puzzles. Truth "
        "tellers vs liars, knights and knaves, syllogisms, deductive "
        "constraints over a small set of entities."
    ),
    "geometry": (
        "Plane or solid geometry problems. Triangles, circles, polygons, "
        "areas, volumes, angle chasing, similar triangles. Numerical answer."
    ),
    "probability": (
        "Discrete probability problems — counting favorable outcomes, "
        "conditional probability, expected value, simple combinatorics. "
        "Numerical answer."
    ),
    "arithmetic": (
        "Multi-step arithmetic / mental math. Percentages, ratios, "
        "compound operations. Should require careful tracking, not just rote."
    ),
    "counting": (
        "Combinatorial counting problems. Permutations, combinations, "
        "inclusion-exclusion, lattice paths, arrangements with constraints."
    ),
    "number_theory": (
        "Elementary number theory. GCD/LCM, modular arithmetic, primes, "
        "divisibility, digit puzzles. Integer answer."
    ),
    "set_theory": (
        "Set operations, Venn diagrams, cardinality, inclusion-exclusion, "
        "bijection arguments over small finite sets."
    ),
    "sequences": (
        "Arithmetic/geometric/recursive sequences and series. Find the "
        "n-th term, sum a series, identify the pattern, solve a recurrence."
    ),
    "inequalities": (
        "Algebraic or arithmetic inequality problems. AM-GM, rearrangement, "
        "bounding, Cauchy-Schwarz at the very basic level."
    ),
    "algebra_word_problems": (
        "Classic algebra word problems — rate × time, mixture, work, age, "
        "money. Single variable or small system; numerical answer."
    ),
}


def _user_prompt(category: str, idx: int) -> str:
    guidance = CATEGORY_GUIDANCE[category]
    return (
        f"Category: {category}\n"
        f"Guidance: {guidance}\n\n"
        f"Generate problem #{idx + 1} in this category. Output ONLY the "
        f"problem statement — no labels, no answer, no commentary."
    )


async def _generate_one(judge: AnthropicJudge, category: str, idx: int) -> dict:
    text = await judge.call(system=SYSTEM_PROMPT, user=_user_prompt(category, idx))
    text = text.strip()
    return {
        "id": f"{category}_{idx:03d}",
        "category": category,
        "prompt": text,
    }


async def _generate_all(
    categories: list[str],
    per_category: int,
    model: str,
    rpm: int,
    max_concurrent: int,
) -> list[dict]:
    judge = AnthropicJudge(model=model, max_tokens=512, max_concurrent=max_concurrent, rpm=rpm)
    tasks = [
        _generate_one(judge, cat, i)
        for cat in categories
        for i in range(per_category)
    ]
    log.info("[info] dispatching %d prompt-gen tasks across %d categories", len(tasks), len(categories))
    results = await asyncio.gather(*tasks)
    log.info("[info] anthropic calls=%d errors=%d", judge.n_calls, judge.n_errors)
    # Drop any empties from retry exhaustion.
    results = [r for r in results if r["prompt"]]
    return results


def _stamp_splits(prompts: list[dict], n_eval: int, seed: int) -> list[dict]:
    """Tag each prompt with split={'dom','eval'} via category-stratified sampling.

    The DoM steering vector is fit on `dom`-split traces; the magnitude
    sweep at the end uses only `eval`-split prompts. Without this hold-out
    the eval prompts' own (unsteered) traces sit inside D, making the
    Fig 3 reproduction technically in-sample. Stratification keeps each
    of the 10 categories represented in both splits.
    """
    import random
    rng = random.Random(seed)
    by_cat: dict[str, list[dict]] = {}
    for p in prompts:
        by_cat.setdefault(p["category"], []).append(p)
    cats = sorted(by_cat)
    per_cat_eval = max(1, n_eval // len(cats))
    eval_ids: set[str] = set()
    for cat in cats:
        rng.shuffle(by_cat[cat])
        for p in by_cat[cat][:per_cat_eval]:
            eval_ids.add(p["id"])
    out = []
    for p in prompts:
        out.append({**p, "split": "eval" if p["id"] in eval_ids else "dom"})
    n_eval_actual = sum(1 for p in out if p["split"] == "eval")
    log.info("[info] holdout split | dom=%d eval=%d (target eval=%d)",
             len(out) - n_eval_actual, n_eval_actual, n_eval)
    return out


def main(argv: list[str] | None = None) -> int:
    p = argparse.ArgumentParser(description=__doc__.splitlines()[0])
    p.add_argument("--config", type=Path, default=Path(__file__).parent / "config.yaml")
    p.add_argument("--out", type=Path, default=None, help="override paths.prompts in config")
    p.add_argument("--rpm", type=int, default=40)
    p.add_argument("--max-concurrent", type=int, default=8)
    p.add_argument("--force", action="store_true", help="overwrite existing prompts.json")
    args = p.parse_args(argv)

    cfg = yaml.safe_load(args.config.read_text())
    out_path = Path(args.out or cfg["paths"]["prompts"])

    if out_path.exists() and not args.force:
        existing = json.loads(out_path.read_text())
        log.info("[info] resume | %d existing prompts at %s — pass --force to regenerate", len(existing), out_path)
        return 0

    categories = cfg["seed_prompts"]["categories"]
    per_cat = int(cfg["seed_prompts"]["per_category"])
    model = cfg["seed_prompts"]["generator_model"]

    out_path.parent.mkdir(parents=True, exist_ok=True)
    prompts = asyncio.run(_generate_all(
        categories=categories,
        per_category=per_cat,
        model=model,
        rpm=args.rpm,
        max_concurrent=args.max_concurrent,
    ))
    n_eval = int(cfg["eval"]["n_prompts"])
    eval_seed = int(cfg["eval"]["seed"])
    prompts = _stamp_splits(prompts, n_eval=n_eval, seed=eval_seed)

    out_path.write_text(json.dumps(prompts, indent=2))
    log.info("[done] saved %d prompts | path=%s", len(prompts), out_path)
    return 0


if __name__ == "__main__":
    sys.exit(main())
